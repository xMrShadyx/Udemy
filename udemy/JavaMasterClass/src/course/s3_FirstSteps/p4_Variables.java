package course.s3_FirstSteps;

public class p4_Variables {
    public static void main(String[] args) {
        // Used to define a variable by indicating the data type, and the name, and optionally to set
        // the variable to a certain value.

        int myFirstNumber = 5;
        myFirstNumber = 10;
        myFirstNumber = 5 + 10;
        myFirstNumber = (5 + 10) + (2 * 10);
        // Java operators or just operators perform an operation (hence the word) on a variable or
        // value. +, -, / and * are four common ones that I feel sure you are familiar with.
        System.out.println(myFirstNumber);


        // Here the type (short for Data type) in an int, the name is myFirstNumber and the value we are assigning or initializing our
        // variable with is 5. We are declaring a variable of type int with the name myFirstNumber and assigning the value 5 to it.
    }
}


