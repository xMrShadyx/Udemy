package course.s3_FirstSteps;

public class p15_abbreviatingOperators {
    public static void main(String[] args) {
        int result = 5;
        // result = result + 1;
        result++; // 1 + 5 = 6
        System.out.println(result);
        result--; // 6 - 1 = 5;
        System.out.println(result);
        result += 2;
        result *= 10;
        result /= 2;
    }
}
